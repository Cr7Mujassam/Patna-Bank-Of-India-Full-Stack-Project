package ModelPackage;

//class for storing the customer data
public class OpenNewAccountModel {
	
	private String customerName;
	
	private String customerAddress;
	
	private String customerMobileNo;
	
	private String customerDepositAmount;

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getCustomerAddress() {
		return customerAddress;
	}

	public void setCustomerAddress(String customerAddress) {
		this.customerAddress = customerAddress;
	}

	public String getCustomerMobileNo() {
		return customerMobileNo;
	}

	public void setCustomerMobileNo(String customerMobileNo) {
		this.customerMobileNo = customerMobileNo;
	}

	public String getCustomerDepositAmount() {
		return customerDepositAmount;
	}

	public void setCustomerDepositAmount(String customerDepositAmount) {
		this.customerDepositAmount = customerDepositAmount;
	}

	
}
